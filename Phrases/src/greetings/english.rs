//! This module contains English phrases.
//! 
//! # Examples
//! ```
//! let username = "John";
//! println!("{}, {}!",
//!   Phrases::greetings::english::hello(),
//!   username
//! );
//! ```


/// Applies to code below
/// In this case it 's our function `hello()` function.
pub fn hello() -> String {"hello".to_string()}
pub fn goodbye() -> String {"bye".to_string()}